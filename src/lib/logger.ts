/**
 * Logging and report generation utilities
 * Creates datetime-stamped logs and human-friendly markdown reports
 */

import path from 'path';
import {
  getDatetimeString,
  formatDatetimeLog,
  formatDatetimeHuman
} from './datetime';
import { safeWriteFile, getDataDirectories } from './filesystem';

export interface LogMetadata {
  query: string;
  searchEngine: 'brave' | 'firecrawl';
  articleCount: number;
  limit: number;
  responseTime: string;
  status: 'success' | 'error';
  errorMessage?: string;
  timestamp: Date;
}

/**
 * Generates plain text log content
 */
function generateLogContent(metadata: LogMetadata): string {
  const startLog = formatDatetimeLog(metadata.timestamp);
  const lines: string[] = [
    `${startLog} API Call Started`,
    `Query: "${metadata.query}"`,
    `Search Engine: ${metadata.searchEngine}`,
    `Article Count Requested: ${metadata.limit}`,
    '---',
  ];

  if (metadata.status === 'success') {
    const endTime = new Date(metadata.timestamp.getTime() + parseFloat(metadata.responseTime) * 1000);
    const endLog = formatDatetimeLog(endTime);
    lines.push(
      `${endLog} API Call Completed`,
      `Results: ${metadata.articleCount} articles`,
      `Response Time: ${metadata.responseTime}`,
      `Status: Success`
    );
  } else {
    const endLog = formatDatetimeLog(new Date());
    lines.push(
      `${endLog} API Call Failed`,
      `Error: ${metadata.errorMessage || 'Unknown error'}`,
      `Response Time: ${metadata.responseTime}`,
      `Status: Error`
    );
  }

  return lines.join('\n') + '\n';
}

/**
 * Generates markdown report content
 */
function generateReportContent(metadata: LogMetadata, topResults?: Array<{ title: string; url: string; siteName?: string }>): string {
  const humanTime = formatDatetimeHuman(metadata.timestamp);
  const statusEmoji = metadata.status === 'success' ? '✅' : '❌';

  const lines: string[] = [
    '# Search Report',
    '',
    `**Date:** ${humanTime}`,
    `**Query:** "${metadata.query}"`,
    `**Search Engine:** ${metadata.searchEngine === 'brave' ? 'Brave Search API' : 'Firecrawl'}`,
    '',
    '## Results Summary',
    '',
    `- **Articles Retrieved:** ${metadata.articleCount}`,
    `- **Response Time:** ${metadata.responseTime}`,
    `- **Status:** ${statusEmoji} ${metadata.status === 'success' ? 'Success' : 'Error'}`,
  ];

  if (metadata.status === 'error' && metadata.errorMessage) {
    lines.push('', `**Error:** ${metadata.errorMessage}`);
  }

  if (metadata.status === 'success' && topResults && topResults.length > 0) {
    lines.push('', '## Top Results', '');
    topResults.slice(0, 10).forEach((result, index) => {
      const siteName = result.siteName ? ` - ${result.siteName}` : '';
      lines.push(`${index + 1}. [${result.title}](${result.url})${siteName}`);
    });
  }

  lines.push('', '---', `*Generated by AI News Hub*`);

  return lines.join('\n') + '\n';
}

/**
 * Saves log and report files for an API call
 * @returns Object with filenames of saved log and report
 */
export async function saveLogAndReport(
  metadata: LogMetadata,
  topResults?: Array<{ title: string; url: string; siteName?: string }>
): Promise<{ logFilename: string; reportFilename: string }> {
  const datetimeStr = getDatetimeString(metadata.timestamp);
  const { logs: logsDir, reports: reportsDir } = getDataDirectories();

  // Generate filenames
  const logFilename = `log_${datetimeStr}.txt`;
  const reportFilename = `report_${datetimeStr}.md`;

  // Generate content
  const logContent = generateLogContent(metadata);
  const reportContent = generateReportContent(metadata, topResults);

  // Save files
  await Promise.all([
    safeWriteFile(path.join(logsDir, logFilename), logContent),
    safeWriteFile(path.join(reportsDir, reportFilename), reportContent),
  ]);

  console.log(`Saved log: ${logFilename}, report: ${reportFilename}`);

  return { logFilename, reportFilename };
}
